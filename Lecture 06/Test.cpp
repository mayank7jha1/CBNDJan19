#include<iostream>
using namespace std;


//Q. Print a counting from 1 to n.


int main() {

	cout << "Mayank" << endl;

	//Definition of a variable.
	int n;
	cin >> n;

	int count{1};//Starting Point of Repeat Statement.


	//Ending Point of Repeat Statement
	while (count <= n) {


		//Task:
		cout << count << " ";



		//Next State:
		count += 1;//Compound Assignment
	}

	// count += 1;


}